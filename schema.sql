CREATE DATABASE my_schema;

USE my_schema;

CREATE TABLE profile (
	id INT(10) NOT NULL,
    first_name VARCHAR(255),
    last_name VARCHAR(255),
    email VARCHAR(255),
    location VARCHAR(255),
    concerts VARCHAR(255),
    outdoor VARCHAR(255),
    fine_arts VARCHAR(255),
    sports VARCHAR(255),
    festivals VARCHAR(255),
    comedy VARCHAR(255),
    italian VARCHAR(255),
    mexican VARCHAR(255),
    asian VARCHAR(255),
    seafood VARCHAR(255),
    american VARCHAR(255),
    primary key (id)
);
);


